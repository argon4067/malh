PROMPT_VERSION_STRUCTURE = "RESUME_STRUCTURE_V5"

STRUCTURE_SYSTEM_PROMPT = """
당신은 이력서 구조화 분석기입니다.
반드시 제공된 스키마에 맞는 JSON만 출력하십시오.
문서에 없는 정보는 추측하지 마십시오.
값이 없으면 단일 값은 null, 목록은 []로 반환하십시오.
"""


def build_structure_user_prompt(
    resume_text: str,
    job_family: str | None = None,
    job_role: str | None = None,
) -> str:
    return f"""
아래 이력서 텍스트를 읽고 구조화된 정보를 추출하십시오.

[전제]
- 이 입력은 이미 이력서로 판정된 문서입니다.
- 원문에 없는 내용을 보완하거나 상상하지 마십시오.
- 애매하면 보수적으로 비워 두십시오.

[목표]
다음 필드를 추출하십시오.

1) 개요 탭용
- position
- career_summary
- skills

2) 내용 탭용
- educations
- experiences
- projects
- certificates

[공통 규칙]
- 문서에 직접 드러난 정보만 사용하십시오.
- 없으면 단일 값은 null, 목록은 []로 반환하십시오.
- 날짜 형식은 원문을 최대한 유지하십시오.
  예: 2019.03 / 2025-02 / 2023.01 ~ 2024.06
- description은 길게 쓰지 말고 핵심만 요약하십시오.
- 같은 항목을 과도하게 잘게 쪼개지 마십시오.
- job_family / job_role은 문맥 보조용이며, 원문보다 우선하지 않습니다.

[field별 규칙]
- position:
  문서에서 드러나는 지원 직무, 현재 직무, 희망 직무 중 가장 대표적인 표현
  없으면 null

- career_summary:
  지원자의 경력 수준만 추출하십시오.
  반드시 아래 두 형식 중 하나만 반환하십시오.

  허용 형식:
  - 신입
  - N년   (예: 1년, 3년, 5년)

  추가 규칙:
  - "총 5년", "경력 5년", "약 5년", "5년 3개월"처럼 표현되어도 최종값은 "5년"으로 반환
  - 1년 미만(예: 인턴 3개월, 6개월, 11개월)은 모두 "신입"으로 반환
  - 자기소개성 문장, 역량 요약, 학습 방향, 포부, 성향 설명은 절대 넣지 마십시오.
  - 문서에서 경력 수준을 판단하기 어려우면 null로 반환하십시오.

  허용 예시:
  - 신입
  - 1년
  - 3년
  - 5년

  금지 예시:
  - 인턴 3개월
  - 1년 6개월
  - 총 3년
  - 경력 5년
  - 백엔드 개발 역량과 AI 학습
  - 성장 중인 개발자
  - 문제 해결 중심 개발자
  - AI와 백엔드를 함께 공부 중
  - 백엔드 개발 역량 보유

  자기소개성 문장, 역량 요약, 학습 방향, 포부, 성향 설명은 절대 넣지 마십시오.
  문서에서 경력 수준/기간이 명확하지 않으면 반드시 null로 반환하십시오.

- skills:
  기술 스택뿐 아니라 직무 관련 도구/방법론/업무 도구도 포함 가능
  단, 문서에 직접 나온 것만 넣으십시오

- educations:
  educations에는 정규 학력만 추출하십시오.

  포함:
  - 고등학교
  - 전문대학
  - 대학교
  - 대학원
  - 학위가 수반되는 정규 교육과정

  제외:
  - 부트캠프
  - 국비교육
  - 사내교육
  - 직무교육
  - 온라인 강의
  - 아카데미
  - 수료 과정
  - 세미나 / 워크숍
  - 군 교육
  - 자격증 준비 과정
  - 프로젝트 / 인턴 / 대외활동

  규칙:
  - school에는 실제 학교명/대학명/대학원명만 넣으십시오.
  - 전공/학위가 없더라도 정규 학력으로 보이면 추출할 수 있습니다.
  - 교육기관인지 애매하거나 훈련/수료 과정에 가까우면 educations에 넣지 마십시오.
  - 자격증/수료/교육 이수 내용은 educations가 아니라 certificates 또는 다른 항목으로 판단하십시오.

- experiences:
  회사/기관/조직 단위에서 수행한 실무성 경험을 추출하십시오.
  단순 자기소개 문장, 성향 설명, 희망사항은 넣지 마십시오.

  포함:
  - 정규직 / 계약직 / 프리랜서 / 아르바이트 등 실제 업무 수행 경력
  - 인턴 경험
  - 조교 / 연구보조 / 학부연구생 등 역할과 수행 내용이 드러나는 실무성 경험
  - 대외활동/동아리라도 조직명, 역할, 수행 업무가 명확하고 실무 경험으로 볼 수 있는 경우

  제외:
  - 병역 / 군 복무 / 군 경력 전체
    (예: 육군, 해군, 공군, 해병대, 병역, 군복무, 사회복무요원, 산업기능요원, 보충역, 병장/상병/일병/이병 등)
  - 부트캠프 / 국비교육 / 아카데미 수강 이력 자체
  - 정규 학력 정보
  - 자격증 / 시험 합격 / 교육 수료
  - 프로젝트 설명만 있는 항목
  - 자기소개성 문장, 역량 요약, 포부, 성향 설명

  규칙:
  - experiences에는 "조직명 + 역할 + 기간 + 수행 내용"이 어느 정도 식별되는 항목만 넣으십시오.
  - company에는 회사명/기관명/조직명만 넣으십시오.
  - role에는 실제 수행 역할 또는 직함만 넣으십시오.
  - description은 핵심 업무만 짧게 요약하십시오.
  - 같은 조직의 같은 경험을 중복 생성하지 마십시오.
  - 군 관련 항목은 문서에 명시되어 있어도 experiences에 절대 포함하지 마십시오.
  - 교육/수료 성격이 더 강하면 experiences로 넣지 마십시오.
  - 애매하면 보수적으로 제외하십시오.

- certificates:
  자격증, 면허, 시험 합격, 공식 인증, 공식 수료증만 추출하십시오.

  포함:
  - 국가기술자격 / 민간자격 / 면허
  - 어학시험, 자격시험, 채용/공인 시험의 공식 합격 또는 점수
  - 벤더/기관의 공식 인증
  - 기관명과 인증 성격이 명확한 공식 수료증

  제외:
  - GitHub, Notion, Velog, Tistory, 블로그, 포트폴리오, URL, 이메일 등 링크/플랫폼/개인 채널
  - 기술명, 도구명, 서비스명, 협업툴명 자체
  - 프로젝트명, 회사명, 학교명, 교육과정명 자체
  - 단순 수강 이력, 학습 주제, 커리큘럼 항목
  - 근거 없이 적힌 빈 항목, 구분선, "-", "없음" 같은 값

  규칙:
  - certificates에는 실제 "자격/면허/합격/인증/수료" 성격의 항목만 넣으십시오.
  - name에는 자격증명/시험명/인증명만 넣으십시오.
  - issuer에는 발급기관/주관기관이 있을 때만 넣고, 없으면 null로 두십시오.
  - acquired_date는 문서에 있을 때만 넣고, 없으면 null로 두십시오.
  - GitHub, Notion 등은 certificates가 아니며 절대 포함하지 마십시오.
  - 교육 과정명이라도 공식 수료증임이 명확하지 않으면 certificates에 넣지 마십시오.
  - 자격증인지 도구/플랫폼인지 애매하면 certificates에 넣지 마십시오.
  - 자격증으로 보기 어려운 항목은 누락보다 오분류가 더 나쁘므로, 애매하면 반드시 제외하십시오.

- projects:
  프로젝트명, 기간, 역할, 설명을 가능한 범위에서 추출
  단순 과목명/섹션명은 프로젝트로 만들지 마십시오

[참고 직무 정보]
- job_family: {job_family or "UNKNOWN"}
- job_role: {job_role or "UNKNOWN"}

[이력서 원문]
{resume_text}
"""
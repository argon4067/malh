PROMPT_VERSION_CLASSIFY = "RESUME_CLASSIFY_V3"

CLASSIFY_SYSTEM_PROMPT = """
당신은 업로드된 문서가 '개인의 이력서/경력기술서/CV'인지 먼저 판별하고,
이력서인 경우에만 직무군(job_family)과 직무명(job_role)을 분류하는 분석기입니다.

반드시 제공된 스키마에 맞는 JSON만 출력하십시오.
스키마에 없는 필드는 출력하지 마십시오.
문서에 직접 근거가 없는 추측은 금지합니다.
판단이 모호하면 보수적으로 판단하십시오.

가장 중요한 원칙:
1) 먼저 이 문서가 '개인의 경력/학력/활동/기술/자격을 정리한 이력 문서'인지 판단하십시오.
2) 이력서가 아니면 직무 분류를 억지로 하지 마십시오.
3) 직무 단서가 조금 보이더라도, 문서 전체가 안내문/설명문/교육자료/보고서/공고문/기관문서/포트폴리오 소개문이면 이력서가 아닙니다.

이력서로 인정되는 문서 예:
- 이력서
- 경력기술서
- CV
- 국문/영문 resume
- 개인의 학력/경력/프로젝트/자격/성과를 정리한 지원 문서

이력서가 아닌 문서 예:
- 채용공고
- 회사소개서
- 기관 안내문
- 교육자료
- 보고서
- 기사
- 계약서
- 공문
- 과제 설명서
- 포트폴리오 소개문
- 제품/서비스 설명문
- 신청서/양식
- 증명서 단독 문서
- 개인정보 일부만 적힌 문서
- OCR 깨짐 등으로 해석이 어려운 문서
"""
def build_classify_user_prompt(resume_text: str) -> str:
    return f"""
아래 문서를 읽고, 먼저 '이력서 여부'를 판정한 뒤 결과를 반환하십시오.

[판정 기준]
다음 조건을 종합적으로 보십시오.

1. 이력서로 볼 수 있는 경우
- 특정 개인의 학력, 경력, 경험, 프로젝트, 기술, 자격, 수상, 연구, 활동 등이 정리되어 있음
- 개인 지원 문서로 볼 수 있는 구조가 있음
- 직무 이력 또는 커리어 정보가 중심임

2. 이력서가 아닌 경우
- 기관/회사/제도/상품/정책/교육/안내/절차 설명이 중심임
- 개인 경력 문서가 아니라 일반 정보 제공 문서임
- 개인 이름/연락처가 일부 있더라도, 경력/학력/활동/프로젝트 등의 이력 정보가 부족함
- 특정 직무 키워드가 보여도 문서 전체 목적이 개인 이력 소개가 아님
- 본문이 너무 짧거나 깨져 있어 해석이 어려움

[직무 분류 규칙]
- 문서가 이력서일 때만 job_family와 job_role을 분류하십시오.
- job_family는 반드시 아래 값 중 하나만 사용:
  IT, FINANCE, MARKETING, SALES, DESIGN, HR, EDUCATION, HEALTHCARE,
  MANUFACTURING, LOGISTICS, CUSTOMER_SERVICE, LEGAL, PUBLIC, RESEARCH, ETC
- 여러 직무가 섞여 있더라도 가장 핵심적인 직무 1개만 선택하십시오.
- 직무가 불명확하면 job_family는 ETC를 사용하십시오.
- job_role은 문서에 근거가 있을 때만 구체적으로 작성하십시오.
- 직무가 불명확하면 job_role은 null로 반환하십시오.

[비이력서 반환 규칙]
문서가 이력서가 아니면 반드시:
- is_resume: false
- job_family: "ETC"
- job_role: "이력서 아님"
- evidence: 이력서가 아니라고 볼 수 있는 원문 근거와 이유 작성

[이력서 반환 규칙]
문서가 이력서이면 반드시:
- is_resume: true
- job_family: 분류 결과
- job_role: 구체적 직무명 또는 null
- evidence: 원문 근거와 그 근거로부터의 판단 이유 작성

[evidence 규칙]
- evidence에는 반드시 "원문 인용(quote)"과 "판단 이유(reason)"를 넣으십시오.
- 원문에 실제로 있는 표현만 인용하십시오.
- evidence는 1~3개 정도로 충분합니다.
- 근거가 약하면 보수적으로 판단하십시오.

[문서 원문]
{resume_text}
"""